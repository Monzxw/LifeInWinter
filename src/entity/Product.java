package entity;
public class Product {
    String Product_id;
    String Product_title;
    double Product_price;
    int Product_small;
    int Product_Medium;
    int Product_large;
    String Product_picture;
    int Product_hot;

    public String getProduct_id() {
        return Product_id;
    }

    public void setProduct_id(String product_id) {
        Product_id = product_id;
    }

    public String getProduct_title() {
        return Product_title;
    }

    public void setProduct_title(String product_title) {
        Product_title = product_title;
    }

    public double getProduct_price() {
        return Product_price;
    }

    public void setProduct_price(double product_price) {
        Product_price = product_price;
    }

    public int getProduct_small() {
        return Product_small;
    }

    public void setProduct_small(int product_small) {
        Product_small = product_small;
    }

    public int getProduct_Medium() {
        return Product_Medium;
    }

    public void setProduct_Medium(int product_Medium) {
        Product_Medium = product_Medium;
    }

    public int getProduct_large() {
        return Product_large;
    }

    public void setProduct_large(int product_large) {
        Product_large = product_large;
    }

    public String getProduct_picture() {
        return Product_picture;
    }

    public void setProduct_picture(String product_picture) {
        Product_picture = product_picture;
    }

    public int getProduct_hot() {
        return Product_hot;
    }

    public void setProduct_hot(int product_hot) {
        Product_hot = product_hot;
    }
}